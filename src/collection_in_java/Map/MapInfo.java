//What is Map?
//Map is an interface which is part of java.util package.
//Map is used to store data in key and value pair.
//Map interface is a part of Java Collection Framework, but it doesn't inherit Collection Interface.
//Map interface is implemented by HashMap, LinkedHashMap, TreeMap, Hashtable and Properties classes.
//Map interface provides three collection views, which allow a map's contents to be viewed as a set of keys, collection of values, or set of key-value mappings.
//Map interface is not a subtype of the Collection interface. Therefore it behaves little different from the rest of the collection types.

