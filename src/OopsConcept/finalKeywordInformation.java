    //why string is immutable in java?
    //String is immutable in java because java uses the string pool.
    //String pool is a fixed area in heap memory.

    //scp?
    //String Constant Pool (SCP) is a special memory location in Java heap.
    //When we create a string using string literal, that string will be stored in SCP.

    //== vs equals()?
    //== is an operator whereas equals() is a method.
    //== is used to compare the reference of two objects whereas equals() is used to compare the content of two objects.


